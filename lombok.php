<p>2. Taman Wisata Pusuk Sembalu </p>
 <img src="pusuk.jpg">
<p> Taman Wisata Pusuk Sembalun berlokasi di kawasan lereng Gunung Rinjani, Desa Sembalun Bumbung, Kabupaten Lombok Timur. Spot wisata ini menyuguhkan semacam gardu pandang dengan pemandangan unik di ketinggian sekitar 1.250 meter di atas permukaan laut.

Sejauh mata memandang, kamu akan dimanjakan dengan hijaunya hutan dan pegunungan. Selain menyegarkan, tempat ini juga Instagramable banget, lho.</p>

<p>3. Pantai Tanjung Aan </p>
 <img src="aan.jpg">
<p> Pantai Tanjung Aan berada di Lombok Tengah bagian selatan. Pantai ini sangat terjangkau, karena hanya berjarak sekitar 15 menit dari pusat Kota Mataram

Pantai Tanjung Aan menawarkan panorama yang sangat memesona. Pasirnya putih dan halus, airnya pun jernih kebiruan. Gak heran kalau pantai satu ini sanggup menyegarkan mata dan pikiranmu. </p>

<p>4. Rumah Pohon Gangga </p>
<img src="pohon.jpg">
<p> Rumah Pohon Gangga memiliki konsep serupa dengan Taman Wisata Pusuk Sembalun, yakni gardu pandang yang berada di ketinggian. Rumah Pohon Gangga berada di Desa Gangga Mumas, Lombok Utara.

Di tempat ini kamu juga bisa memanjakan matamu dengan pemandangan serba hijau. Lokasinya yang dekat dengan air terjun juga jadi satu hal yang menambah keelokan Rumah Pohon Gangga. </p>

<p>5. Bukit Selong </p>
<img src="bukit.jpg">
<p> Bukit Selong atau Bukit Beleq berlokasi di Desa Sembalun Lawang, Kecamatan Sembalun, Kabupaten Lombok Timur. Dari atas Bukit Selong, kamu akan dimanjakan dengan pemandangan yang sangat yakni bukit-bukit yang indah, serta hamparan sawah yang berwarna-warni </p>
