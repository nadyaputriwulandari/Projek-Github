<p>2. Bukit Kali Kuning </p>
 <img src="kali.jpg">
<p> Dulunya tempat wisata di Yogyakarta ini pernah ramai wisatawan sebelum letusan Gunung Merapi tahun 2010 silam. Pada awal 2017, Bukit Kali Kuning bersemi kembali menjadi tempat wisata baru yang kece banget buat foto-foto.

Duduk santai sambil menikmati indahnya Gunung Merapi dan hutan sekitarnya yang tenang menjadi kegiatan yang paling menyenangkan. Apalagi ditemani dengan secangkir teh hangat atau kopi panas.</p>

<p>3. Taman Pelangi </p>
 <img src="pelangi.jpg">
<p> Masih di utara Yogyakarta, kamu bisa menikmati suasana malam bersama ratusan lampion di sini. Taman Pelangi masih satu kawasan dengan Museum Monumen Jogja Kembali di Jalan Ring Road Utara, Sariharjo, Kabupaten Sleman. Kamu bisa mengunjunginya saat malam, jadi gak cuma ke Alun-alun Kidul atau Malioboro yang sudah ramai pengunjung deh.</p>

<p>4. Kalibiru</p>
<img src="kalibiru.jpg">
