<p>2. Pura Tanah Lot </p>
 <img src="pura.jpg">
<p> Pura Tanah Lot adalah salah satu tempat wisata di Bali yang terkenal dengan keindahannya, terutama pada saat matahari terbenam. Pura Tanah Lot yang terdiri dari 2 buah pura merupakan pura tempat memuja dewa laut.

Keunikan utama dari Pura Tanah Lot adalah pura ini berada di atas sebuah batu karang besar di pinggir laut. Pada saat air laut pasang, anda tidak dapat mendekat ke Pura Tanah Lot karena di sekitar batu karang penyangga Pura Tanah Lot akan digenangi air laut. Pada saat air surut anda dapat melihat beberapa ular laut jinak yang menurut penduduk setempat merupakan penjaga Pura Tanah Lot. Selain itu, di lokasi wisata Bali ini anda juga dapat memegang ular suci yang jinak dan tidak berbahaya.</p>

