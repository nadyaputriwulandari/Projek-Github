 <img src="uluwatu.jpg">
<p> Pura Uluwatu adalah salah satu tempat wisata di Bali yang berada di atas sebuah tebing yang menjorok ke laut. Pura Uluwatu tidak hanya menawarkan suasana religius khas Bali, namun juga menawarkan keindahan panorama, terutama keindahan pemandangan matahari tenggelamnya yang sudah sangat terkenal. Di tempat wisata di Bali yang satu ini, anda akan berjumpa dengan sejumlah kera yang dipercaya berfungsi menjaga kesucian Pura Uluwatu. Untuk memasuki area Pura Uluwatu, anda harus menggunakan sarung dan selendang yang merupakan simbol hormat kepada kesucian Pura Uluwatu. </p>

